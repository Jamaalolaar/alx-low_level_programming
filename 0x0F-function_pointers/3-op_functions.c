#include "3-calc.h"

/**
 * op_add - addition
 * @a: first entry
 * @b: second entry
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction
 * @a: first entry
 * @b: second entry
 * Return: subtraction of a from b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: first entry
 * @b: second entry
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: first entry
 * @b: second entry
 * Return: division of a by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: first entry
 * @b: second entry
 * Return: modulo of a and b
 */

int op_mod(int a, int b);
{
	return (a % b);
}
